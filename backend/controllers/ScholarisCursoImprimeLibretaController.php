    private function inserta_cursos($periodo){
        $con = \Yii::$app->db;
        $query = "insert into scholaris_curso_imprime_libreta(curso_id, imprime, rinde_supletorio)
                    select 	c.id, 'NO', 0
                    from 	op_course c
                                    inner join op_section s on s.id = c.section
                                    inner join scholaris_op_period_periodo_scholaris sop on sop.op_id = s.period_id
                                    inner join scholaris_periodo p on p.id = sop.scholaris_id
                    where	p.id = $periodo
                                    and c.id not in (select curso_id from scholaris_curso_imprime_libreta);";
        $con->createCommand($query)->execute();
    }

